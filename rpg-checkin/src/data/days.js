function cleanVideoTitle(title) {
  return title
    .replace(/^\d+\.\d+\s*-\s*/, "")
    .replace(/\.mp4$/i, "")
    .replace(/\s+s$/i, "")
    .trim();
}

function buildDay(day, title, summary, milestone, totalXP, videoTitles) {
  const slots = videoTitles.length + 1;
  const perVideoXP = Math.floor(totalXP / slots);
  const dayBonusXp = totalXP - perVideoXP * videoTitles.length;

  return {
    day,
    title,
    summary,
    milestone,
    xp: totalXP,
    videos: videoTitles.map((rawTitle, index) => ({
      id: `${day}.${index + 1}`,
      title: cleanVideoTitle(rawTitle),
      xp: perVideoXP,
    })),
    dayBonusXp,
  };
}

